        1.敏捷开发
        是一种以用户需求进化为核心、迭代、循序渐进的开发方法。首先把 用户（客户 ）最关注的软件原型做出来，交付或上线，在实际场景中去快速修改弥补需求中的不足，再次发布版本。
        通过一些敏捷实践方式，细化story ，提供更小的迭代。如此循环，直到用户（客户）满意。适用于需求不明确、创新性或者需要抢占市场的项目。
        敏捷开发最大的特点是迭代式开发，各个阶段都具备独立运行和独立交付的特性。主要是以客户的需求进化为核心，采用迭代、循序渐进的方法进行软件开发。
        在敏捷开发中，软件项目在构建初期被切分成多个子项目，即就是多个sprint，每个sprint中又会有划分成多个build，在一个软件项目完成后会对整个产品从头到尾进行回归验证，
        以确保软件在每一次迭代过程中没有错误产生。在软件最后的发布阶段，客户会对最终的产品进行验收工作。上线后对软件进行一直的维护。
        
        Keep 的开发流程是基于 Scrum ，该流程要求在特定的一段时间内，输出确定的需求，在开发中需求尽量不要变更，开发人员快速完成开发。
        当前，Keep 的迭代和发版周期为两周，保持两周一更新，这样能让用户一直对产品保持新鲜感，也使团队的开发更有节奏。 Keep 技术开发的迭代周期和流程如下图所示：
