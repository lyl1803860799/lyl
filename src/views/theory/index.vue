        <h2>三、软件绿色版和安装版的区别</h2>
        软件的安装版就是从安装文件里释放出来的版本。也就是下载来之后一般是一个安装文件，点了之后，会有选择让我们装在哪个盘，然后它会释放出文件什么的。
        说白了：安装版的软件,就是要安装。
        绿色版一般下来就是一个压缩文件，里边有一个文件夹，文件夹里就包含了这个软件的各种文件。直接把这些文件释放出来，双击那个主程序图标就可以使用了。
        绿色版说白了：就是免安装版。
        把这个文件下载后,直接打开这个文件夹,里面就有一个文件的启动图标,你直接双击就能打开这个程序了,不用你安装到C盘或D盘了,你可以把这个文件夹随意放在任一个盘里,也可以把启动图标在桌面建一个快捷方式也行.
        绿色版怎么来的？
        安装好软件。然后把那个软件的文件所在的文件夹压缩出来，就成了绿色版。

        两者的不同在于。一般安装版的文件，会写入系统注册表，因此在系统里会有该文件的卸载信息。而绿色版的话在系统里就不会有卸载信息。
        安装版有的软件有插件。而绿色版就都不存插件了。
        拿迅雷来举例吧。如果是安装版的迅雷，装好后我们在网页上点右键会显示“使用迅雷下载”。
        而如果直接是下载绿色版的迅雷来用，一开始右键里不会有“使用迅雷下载”的字样，因为绿色版没有像安装版那样有写入插件信息这个步骤，只能是打开迅雷后，在设置里修复一下，才会有。
        具体情况还有很多，因为软件不同情况也不同。大多数软件都支持绿色版，有一些软件因为一定要用到注册表所以绿色版是用不了的。
        安装版有安装要卸载，绿色版的直接删掉就好，不用卸载。

        个人认为绿色版的软件好一点，因为绿色版的不用安装，省时间，且不用写入注册表，减少注册表冗余，直接解压就可以用了。安装版的要占用资源，大小要看软件的体积来决定。同时他们的安全性是没有区别的
